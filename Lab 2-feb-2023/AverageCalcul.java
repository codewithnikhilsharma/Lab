package Object;

public class AverageCalcul {
	
	// Java program that takes five numbers as input to calculate and print
	// the average of the numbers.
   
	public static void main(String[] args) {
		
		
		// Input
		
		int num1 = 10;
		int num2 = 20;
		int num3 = 30;
		int num4 = 40;
		int num5 = 50;
		
		int Average;
		
		 
		// Calculate Average
		 Average = (num1 + num2 + num3 + num4 + num5) / 5;
		 
		 // For output
		 System.out.println("The average of the five numbers is: " + Average);
	
	}
}
