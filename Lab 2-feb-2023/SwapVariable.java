package Object;

public class SwapVariable {
    
	// Swapping two variables
	
	public static void main(String[] args) {
		
		   // input value
			    int x = 10;
			    int y = 20;

			    System.out.println("Before swapping: x = " + x + " and y = " + y);

			    int temp = x;
			    x = y;
			    y = temp;

			    System.out.println("After swapping: x = " + x + " and y = " + y);
			  }		
	        }

